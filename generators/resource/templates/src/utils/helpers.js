exports.nullToUndefined = input => {
  if (Array.isArray(input)) return input;
  const obj = Object.assign(input);
  Object.keys(obj).forEach(key => {
    const value = obj[key];
    if (value === null) {
      obj[key] = undefined;
    } else if (typeof value === 'object') {
      obj[key] = exports.nullToUndefined(value);
    }
  });
  return obj;
};
